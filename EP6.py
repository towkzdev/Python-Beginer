#Type conversion

x = 10
y = 3.14
z = "20"

#data type
print(type(x))
print(type(y))
print(type(z))

#บวกเลข
total_1 = x + y
print(total_1)

# string => int
total_2 = x + int(z)
print(total_2)

#int => string
total_3 = str(x) + z
print(total_3)

#string => float
total_4 = float(z)
float(z)
print(total_4)

#float => string
print(str(y))

#int => float
print(float(x))

#float => int
print(int(y))


#string => float
z = float(z)
print(type(z))

