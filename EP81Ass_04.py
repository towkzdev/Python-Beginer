#หอคอยฮานอย Tower of hanoi

'''
n=จำนวนจาน
เสา => A,B,C

มีจานจำนวน 3 จาน
n=1
n=2 (n-1)
n=3 (ใหญ่สุด)

'''

'''
A => 3 => C => ใหญ่ => เล็ก

A => ล ก ญ
A (ล ก) =>B(ล ก)
A ญ => C
C= ล ก ญ

'''

def hanoi(n,a,b,c):
    # a=> C
    if n==0:
        return
    hanoi(n-1,a,c,b) #ย้าย a(ล,ก) -> b | c จุดพัก (ย้ายa,พักc,ไปb)
    print("จานที่ = ",n,"จาก =",a,"ไปที่ =",c)
    hanoi(n-1,b,a,c)

hanoi(3,"A","B","C")